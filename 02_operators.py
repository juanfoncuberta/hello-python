print(15 % 4)   #Modulus
print(16 / 4)   #Division
print(15 / 4)   #Division
print(16 // 4)  #Division without the remainder
print(15 // 4)  #Division without the remainder
print(3 ** 3)   #Exponential





#Operators comparison
print(3 > 2)     # reater than 
print(3 >= 2)    # greater than or equal
print(2 < 3)     # less than
print(2 <= 3)    # less than or equal
print(3 == 2)    # equal
print(3 != 2)    # not equal


